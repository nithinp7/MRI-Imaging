		glEnableVertexAttribArray(0);
		glVertexAttribIPointer(0, 3, GL_INT/*, GL_FALSE*/, sizeof(glm::ivec3), (void*)0);
		
		glBindVertexArray(0);
		
		glGenBuffers(1, &buf);

